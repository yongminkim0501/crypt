static const uint8_t Rcon[11] = {0x8d, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36};

static const uint8_t M[16] = {2, 3, 1, 1, 1, 2, 3, 1, 1, 1, 2, 3, 3, 1, 1, 2};

static const uint8_t IM[16] = {0x0e, 0x0b, 0x0d, 0x09, 0x09, 0x0e, 0x0b, 0x0d, 0x0d, 0x09, 0x0e, 0x0b, 0x0b, 0x0d, 0x09, 0x0e};

/*
 * Generate an AES key schedule
 */
static void AddRoundKey(uint8_t *state, const uint32_t *roundKey)
{
    for (int i = 0; i < 4; i++) {
        // roundKey[i]를 4바이트로 나누어 각각 state의 해당 위치와 XOR
        state[i * 4 + 0] ^= (roundKey[i] >> 24) & 0xFF; // 1st byte
        state[i * 4 + 1] ^= (roundKey[i] >> 16) & 0xFF; // 2nd byte
        state[i * 4 + 2] ^= (roundKey[i] >> 8) & 0xFF;  // 3rd byte
        state[i * 4 + 3] ^= roundKey[i] & 0xFF;         // 4th byte
    }
}

static void SubBytes(uint8_t *state, int mode) {
    for (int i = 0; i < 16; i++) {
        state[i] = (mode == ENCRYPT) ? sbox[state[i]] : isbox[state[i]];
    }
}

static void ShiftRows(uint8_t *state, int mode) {
    uint8_t temp;

    if (mode == ENCRYPT) {
        // Shift row 1
        temp = state[1];
        state[1] = state[5];
        state[5] = state[9];
        state[9] = state[13];
        state[13] = temp;

        // Shift row 2
        temp = state[2];
        state[2] = state[10];
        state[10] = temp;
        temp = state[6];
        state[6] = state[14];
        state[14] = temp;

        // Shift row 3
        temp = state[3];
        state[3] = state[15];
        state[15] = state[11];
        state[11] = state[7];
        state[7] = temp;
    } else {
        // Inverse Shift Rows
        // (각 행을 오른쪽으로 이동)
        temp = state[1];
        state[1] = state[13];
        state[13] = state[9];
        state[9] = state[5];
        state[5] = temp;

        temp = state[2];
        state[2] = state[10];
        state[10] = temp;
        temp = state[6];
        state[6] = state[14];
        state[14] = temp;

        temp = state[3];
        state[3] = state[7];
        state[7] = state[11];
        state[11] = state[15];
        state[15] = temp;
    }
}

static void MixColumns(uint8_t *state, int mode) {
    uint8_t temp[16];

    if (mode == ENCRYPT) {
        for (int c = 0; c < Nb; c++) {
            temp[c * 4 + 0] = (uint8_t)(M[0] * state[c * 4 + 0] ^ M[1] * state[c * 4 + 1] ^ M[2] * state[c * 4 + 2] ^ M[3] * state[c * 4 + 3]);
            temp[c * 4 + 1] = (uint8_t)(M[0] * state[c * 4 + 0] ^ M[1] * state[c * 4 + 1] ^ M[2] * state[c * 4 + 2] ^ M[3] * state[c * 4 + 3]);
            temp[c * 4 + 2] = (uint8_t)(M[0] * state[c * 4 + 0] ^ M[1] * state[c * 4 + 1] ^ M[2] * state[c * 4 + 2] ^ M[3] * state[c * 4 + 3]);
            temp[c * 4 + 3] = (uint8_t)(M[0] * state[c * 4 + 0] ^ M[1] * state[c * 4 + 1] ^ M[2] * state[c * 4 + 2] ^ M[3] * state[c * 4 + 3]);
        }
    } else { // 복호화
        for (int c = 0; c < Nb; c++) {
            temp[c * 4 + 0] = (uint8_t)(IM[0] * state[c * 4 + 0] ^ IM[1] * state[c * 4 + 1] ^ IM[2] * state[c * 4 + 2] ^ IM[3] * state[c * 4 + 3]);
            temp[c * 4 + 1] = (uint8_t)(IM[0] * state[c * 4 + 0] ^ IM[1] * state[c * 4 + 1] ^ IM[2] * state[c * 4 + 2] ^ IM[3] * state[c * 4 + 3]);
            temp[c * 4 + 2] = (uint8_t)(IM[0] * state[c * 4 + 0] ^ IM[1] * state[c * 4 + 1] ^ IM[2] * state[c * 4 + 2] ^ IM[3] * state[c * 4 + 3]);
            temp[c * 4 + 3] = (uint8_t)(IM[0] * state[c * 4 + 0] ^ IM[1] * state[c * 4 + 1] ^ IM[2] * state[c * 4 + 2] ^ IM[3] * state[c * 4 + 3]);
        }
    }

    for (int i = 0; i < 16; i++) {
        state[i] = temp[i];
    }
}

void KeyExpansion(const uint8_t *key, uint32_t *roundKey) { // 문제 없는 코드
    // 초기 키 복사 (리틀 엔디안)
    for (int i = 0; i < Nk; i++) {
        roundKey[i] = ((uint32_t)key[4 * i]) |
                      ((uint32_t)key[4 * i + 1] << 8) |
                      ((uint32_t)key[4 * i + 2] << 16) |
                      ((uint32_t)key[4 * i + 3] << 24);
    }

    // 키 확장
    for (int i = Nk; i < RNDKEYLEN; i++) {
        uint32_t temp = roundKey[i - 1];

        if (i % Nk == 0) {
            // 이전 temp에서 4바이트를 먼저 처리해야 하므로
            uint32_t temp_rot = (temp >> 8) | (temp << 24); // RotWord

            // SubWord
            temp_rot = (sbox[temp_rot & 0xFF]) |
                       (sbox[(temp_rot >> 8) & 0xFF] << 8) |
                       (sbox[(temp_rot >> 16) & 0xFF] << 16) |
                       (sbox[(temp_rot >> 24) & 0xFF] << 24);

            // Rcon 적용
            temp_rot ^= Rcon[i / Nk];

            temp = temp_rot; // temp에 최종 결과 저장
        } else if (Nk > 6 && i % Nk == 4) {
            // SubWord
            temp = (sbox[temp & 0xFF]) |
                   (sbox[(temp >> 8) & 0xFF] << 8) |
                   (sbox[(temp >> 16) & 0xFF] << 16) |
                   (sbox[(temp >> 24) & 0xFF] << 24);
        }
        // roundKey[i] 계산
        roundKey[i] = roundKey[i - Nk] ^ temp;
    }
}





/*
 * AES cipher function
 * If mode is nonzero, then do encryption, otherwise do decryption.
 */
void Cipher(uint8_t *state, const uint32_t *roundKey, int mode) {
    if (mode == ENCRYPT) { // 암호화
        AddRoundKey(state, roundKey); // Initial round key addition

        for (int round = 1; round < Nr; round++) {
            SubBytes(state, mode); // mode 전달
            ShiftRows(state, mode); // mode 전달
            MixColumns(state, mode); // mode 전달
            AddRoundKey(state, roundKey + round * Nb);
        }

        SubBytes(state, mode); // mode 전달
        ShiftRows(state, mode); // mode 전달
        AddRoundKey(state, roundKey + Nr * Nb); // Final round key addition
    } else { // 복호화
        AddRoundKey(state, roundKey + Nr * Nb); // Initial round key addition

        for (int round = Nr - 1; round > 0; round--) {
            ShiftRows(state, mode); // mode 전달
            SubBytes(state, mode); // mode 전달
            AddRoundKey(state, roundKey + round * Nb);
            MixColumns(state, mode); // mode 전달
        }

        ShiftRows(state, mode); // mode 전달
        SubBytes(state, mode); // mode 전달
        AddRoundKey(state, roundKey); // Final round key addition
    }
}
